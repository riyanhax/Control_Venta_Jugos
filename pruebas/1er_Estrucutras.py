def sum (x, y):
    rtsum = x + y
    return rtsum
def res (x, y):
    rtres = x - y
    return rtres
def mut (x, y):
    rtmul = x * y
    return rtmul
def div (x, y):
    rtdiv = x / y
    return rtdiv
def mod (x, y):
    rtmod = x % y
    return rtmod
